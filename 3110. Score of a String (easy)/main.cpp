class Solution {
public:
  int scoreOfString(const std::string& s) {
    auto res = 0;
    const auto n = static_cast<int>(s.size());
    for (auto i=0; i+1<n; ++i) {
      const auto mn = std::min(s[i], s[i+1]);
      const auto mx = std::max(s[i], s[i+1]);
      res += mx - mn;
    }
    return res;
  }
};

