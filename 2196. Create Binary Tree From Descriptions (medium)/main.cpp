struct TreeNode {
  int val;
  TreeNode* left;
  TreeNode* right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  explicit TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode* left, TreeNode* right) : val(x), left(left), right(right) {}
};

class Solution {
public:
  TreeNode* createBinaryTree(const vvi& descriptions) {
    std::unordered_set<int> parents, children;
    std::unordered_map<int, TreeNode*> nodes;
    for (const auto& d : descriptions) {
      if (!nodes.contains(d[0])) nodes[d[0]] = new TreeNode(d[0]);
      if (!nodes.contains(d[1])) nodes[d[1]] = new TreeNode(d[1]);
      if (d[2]) nodes[d[0]]->left = nodes[d[1]];
      else nodes[d[0]]->right = nodes[d[1]];
      parents.insert(d[0]);
      children.insert(d[1]);
    }
    auto root_val = -1;
    for (const auto p : parents)
      if (!children.contains(p)) root_val = p;
    return nodes[root_val];
  }
};

