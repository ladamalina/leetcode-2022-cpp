constexpr int MAX_TI = 1e5;
std::array<int, MAX_TI + 1> t2p, t2u;

class TaskManager {
public:
  explicit TaskManager(const vvi& tasks) {
    for (const auto& t : tasks) {
      const auto ui = t[0], ti = t[1], p = t[2];
      t2p[ti] = p;
      t2u[ti] = ui;
      pt_set.emplace(p, ti);
    }
  }

  void add(const int ui, const int ti, const int p) {
    t2p[ti] = p;
    t2u[ti] = ui;
    pt_set.emplace(p, ti);
  }

  void edit(const int ti, const int new_p) {
    pt_set.erase({t2p[ti], ti});
    pt_set.emplace(new_p, ti);
    t2p[ti] = new_p;
  }

  void rmv(const int ti) {
    pt_set.erase({t2p[ti], ti});
  }
    
  int execTop() {
    if (pt_set.empty())
      return -1;
    const auto it = std::prev(pt_set.end());
    const auto ti = it->S;
    pt_set.erase(it);
    return t2u[ti];
  }

private:
  std::set<ii> pt_set;
};

