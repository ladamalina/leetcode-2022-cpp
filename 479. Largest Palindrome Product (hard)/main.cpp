#define FOR(_i, _a, _b) for (int _i = (_a); _i <= (_b); ++(_i))
#define FORD(_i, _a, _b) for (int _i = (_a); _i >= (_b); --(_i))
#define RNG(_l) (_l).begin(), (_l).end()
#define SORT(_l) std::sort((_l).begin(), (_l).end())
#define CI(_v) static_cast<int>(_v)
#define CL(_v) static_cast<ll>(_v)
#define F first
#define S second

constexpr ll MOD = 1337;
std::string s, s_rev;

class Solution {
 public:
  static int largestPalindrome(int n) {
    if (n == 1) return 9;
    // 2 <= n <= 8
    const auto pref_high = CI(std::pow(10, n)) - 1;
    const auto pref_low = CI(std::pow(10, n-1));
    FORD(pref_num, pref_high, pref_low) {
      s = std::to_string(pref_num);
      s_rev = s;
      std::reverse(RNG(s_rev));
      s += s_rev;
      ll num = std::stoll(s);
      const auto low_bound = std::sqrt(num);
      FORD(j, pref_high, low_bound) {
        if (num % j == 0)
          return num % MOD;
      }
    }
    return 0;
  }
};

