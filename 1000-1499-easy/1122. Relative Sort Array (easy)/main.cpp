constexpr int MAX_V = 1000;

class Solution {
public:
  vi relativeSortArray(vi& arr1, vi& arr2) {
    const auto n2 = CI(arr2.size());
    vi ids(MAX_V+1, n2);
    for (auto i=0; i<n2; ++i)
      ids[arr2[i]] = i;
    std::sort(arr1.begin(), arr1.end(), [&](int v1, int v2) {
        return std::tie(ids[v1], v1) < std::tie(ids[v2], v2);
    });
    return arr1;
  }
};

