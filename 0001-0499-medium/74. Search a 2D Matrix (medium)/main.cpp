class Solution {
public:
  bool searchMatrix(const vvi& g, const int target) {
    const auto n = SZ(g);
    
    int i_lo = 0, i_hi = n-1;
    while (i_lo < i_hi) {
      const auto i_mi = (i_lo + i_hi + 1) / 2;
      if (g[i_mi][0] <= target) i_lo = i_mi;
      else i_hi = i_mi - 1;
    }
    if (g[i_hi][0] > target) return false;

    const auto it = std::lower_bound(RNG(g[i_hi]), target);
    return (it != g[i_hi].end() && *it == target);
  }
};

