[[maybe_unused]] constexpr int MAX_T = 1000;
std::array<int, MAX_T + 1> dp;

class Solution {
public:
  int combinationSum4(vi& nums, const int target) {
    SORT(nums);
    const auto n = SZ(nums);
    std::fill(dp.begin(), dp.begin() + target + 1, -1);
    const std::function<int(int)> rec = [&](int t) {
      if (t == 0) return 1;
      // t > 0
      auto& res = dp[t];
      if (res != -1) return res;
      res = 0;
      for (auto i = 0; i < n && nums[i] <= t; ++i)
        res += rec(t - nums[i]);
      return res;
    };
    const auto res = rec(target);
    return res;
  }
};

