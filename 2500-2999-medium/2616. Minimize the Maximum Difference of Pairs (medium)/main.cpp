bool BinCheck(int lim, const vi& nums, int p) {
  const auto n = SZ(nums);
  auto cnt = 0, i = 0;
  while (i < n - 1) {
    if (std::abs(nums[i+1] - nums[i]) <= lim) {
      i += 2;
      ++cnt;
      if (cnt >= p)
        return true;
    } else {
      i += 1;
    }
  }
  return false;
}

class Solution {
public:
  int minimizeMax(vi& nums, int p) {
    if (p == 0)
      return 0;

    const auto n = SZ(nums);
    SORT(nums);

    ll lo = 0, hi = nums[n-1] - nums[0];
    while (lo < hi) {
      const auto mi = (lo + hi) / 2;
      if (BinCheck(mi, nums, p)) hi = mi;
      else lo = mi + 1;
    }

    return lo;
  }
};

