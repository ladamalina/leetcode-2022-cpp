bool BinCheck(const ll limit, const vi& ranks, const int cars) {
  ll done = 0;
  for (const auto rank : ranks) {
    done += CL(std::sqrt(limit / rank));
  }
  return done >= cars;
}

class Solution {
public:
  ll repairCars(const vi& ranks, const int cars) {
    ll lo = 1, hi = 1e14;
    while (lo < hi) {
      const auto mid = (lo + hi) / 2;
      const auto check = BinCheck(mid, ranks, cars);
      // std::cerr << lo << '\t' << mid << '\t' << hi << '\t' << check << '\n';
      if (check)
        hi = mid;
      else
        lo = mid + 1;
    }
    return lo;
  }
};

[[maybe_unused]] void TestSolution() {
  {
    const auto start_t = std::chrono::high_resolution_clock::now();
  
    const auto a_out = Solution().repairCars({99}, 1000000);
    assert(a_out == 99000000000000ll);
  
    const auto end_t = std::chrono::high_resolution_clock::now();
    const auto total_t = std::chrono::duration_cast<std::chrono::milliseconds>(end_t - start_t).count();
    std::cerr << total_t << " ms"sv << std::endl;
  }
  std::cerr << "TestSolution OK"sv << std::endl;
}

