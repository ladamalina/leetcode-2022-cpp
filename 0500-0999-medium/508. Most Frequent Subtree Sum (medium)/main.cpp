struct TreeNode {
  int val;
  TreeNode* left;
  TreeNode* right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  explicit TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode* left, TreeNode* right) : val(x), left(left), right(right) {}
};

class Solution {
public:
  vi findFrequentTreeSum(TreeNode* root) {
    std::unordered_map<int,int> sum_freq;
    int max_freq = 0;
    vi res;

    const std::function<int(TreeNode*)> dfs = [&](TreeNode* node) {
      const auto sum = node->val + (node->left ? dfs(node->left) : 0) + (node->right ? dfs(node->right) : 0);
      const auto freq = ++sum_freq[sum];
      if (freq > max_freq) {
        max_freq = freq;
        res.clear();
        res.push_back(sum);
      } else if (freq == max_freq) {
        res.push_back(sum);
      }
      return sum;
    };
    [[maybe_unused]] const auto root_sum = dfs(root);
    return res;
  }
};

