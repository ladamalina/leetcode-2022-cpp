class FindSumPairs {
public:
  FindSumPairs(const vi& nums1, const vi& nums2) : arr1_(nums1), arr2_(RNG(nums2)) {
    for (const auto x : arr2_)
      ++x_freq_[x];
  }
    
  void add(int i, int val) {
    auto it = x_freq_.find(arr2_[i]);
    if (it->S == 1) x_freq_.erase(it);
    else --it->S;
    arr2_[i] += val;
    ++x_freq_[arr2_[i]];
  }
    
  int count(int tot) const {
    int res = 0;
    for (const auto x : arr1_) {
      const auto it = x_freq_.find(tot - x);
      if (it != x_freq_.end()) res += it->S;
    }
    return res;
  }

private:
  const vi& arr1_;
  vl arr2_;
  std::unordered_map<ll, int> x_freq_{};
};

