class Solution {
public:
  int minimumSize(const vi& nums, const int limit_ops) {
    auto lo = 1, hi = *std::max_element(RNG(nums));

    const auto bin_check = [&](const int penalty) {
      ll ops = 0;
      for (const auto x : nums) {
        if (x > penalty) {
          const auto x_ops = (x + penalty - 1) / penalty - 1;
          ops += x_ops;
          if (ops > limit_ops)
            return false;
        }
      }
      return true;
    };
    
    while (lo < hi) {
      const auto mid = (lo + hi) / 2;
      if (bin_check(mid))
        hi = mid;
      else
        lo = mid + 1;
    }
    return lo;
  }
};

[[maybe_unused]] void TestSolution() {
  {
    const auto start_t = std::chrono::high_resolution_clock::now();

    const vi nums{9};
    const auto a_out = Solution().minimumSize(nums, 2);
    assert(a_out == 3);
  
    const auto end_t = std::chrono::high_resolution_clock::now();
    const auto total_t = std::chrono::duration_cast<std::chrono::milliseconds>(end_t - start_t).count();
    std::cerr << total_t << " ms"sv << std::endl;
  }
  {
    const auto start_t = std::chrono::high_resolution_clock::now();

    const vi nums{2,4,8,2};
    const auto a_out = Solution().minimumSize(nums, 4);
    assert(a_out == 2);
  
    const auto end_t = std::chrono::high_resolution_clock::now();
    const auto total_t = std::chrono::duration_cast<std::chrono::milliseconds>(end_t - start_t).count();
    std::cerr << total_t << " ms"sv << std::endl;
  }
  std::cerr << "TestSolution OK"sv << std::endl;
}

