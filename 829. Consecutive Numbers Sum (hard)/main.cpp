template<typename T>
std::unordered_set<T> GetFactors(T x) {
  std::unordered_set<T> factors;
  const auto x_sqrt = std::sqrt(x);
  for (T i = 1; i <= x_sqrt; ++i) {
    if (x % i == 0) {
      factors.insert(i);
      factors.insert(x/i);
    }
  }
  return factors;
}

class Solution {
public:
  int consecutiveNumbersSum(ll s) {
    const auto fs = GetFactors(2*s);
    auto res = 0;
    for (const auto n : fs) {
      const auto a2 = 2*s / n - (n-1);
      if (a2 > 0 && a2%2 == 0) {
        ++res;
      }
    }
    return res;
  }
};

