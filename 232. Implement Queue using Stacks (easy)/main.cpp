class MyQueue {
public:
  MyQueue() {}
    
  void push(int x) {
    tail.push(x);
  }
    
  int pop() {
    Rebalance();
    const auto x = head.top();
    head.pop();
    return x;
  }
    
  int peek() {
    Rebalance();
    return head.top();
  }
    
  bool empty() {
    return head.empty() && tail.empty();
  }

private:
  std::stack<int> head, tail;

  void Rebalance() {
    if (!head.empty()) return;
    while (!tail.empty()) {
      const auto x = tail.top();
      tail.pop();
      head.push(x);
    }
  }
};

