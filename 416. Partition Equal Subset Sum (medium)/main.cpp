#define SZ(_v) static_cast<int>((_v).size())
#define F first
#define S second

class Solution {
public:
  bool canPartition(const vi& nums) {
    const auto all_sum = std::accumulate(RNG(nums), 0);
    if (all_sum % 2 == 1)
      return false;
    const auto target_sum = all_sum / 2;

    const auto n = SZ(nums);
    vvi dp(n, vi(target_sum+1, -1));
    
    const std::function<bool(int,int)> rec = [&](const int i, const int l_sum) {
      if (i == n)
        return l_sum == target_sum;
      if (l_sum > target_sum)
        return false;
      if (dp[i][l_sum] != -1)
        return dp[i][l_sum] == 1;

      const auto res = rec(i+1, l_sum + nums[i]) || rec(i+1, l_sum);
      dp[i][l_sum] = res;

      return res;
    };
    const auto res = rec(0, 0);
    return res;
  }
};

[[maybe_unused]] void TestSolution() {
  {
    const auto start_t = std::chrono::high_resolution_clock::now();
  
    const auto a_out = Solution().canPartition({1,5,11,5});
    assert(a_out);
  
    const auto end_t = std::chrono::high_resolution_clock::now();
    const auto total_t = std::chrono::duration_cast<std::chrono::milliseconds>(end_t - start_t).count();
    std::cerr << total_t << " ms"sv << std::endl;
  }
  {
    const auto start_t = std::chrono::high_resolution_clock::now();
  
    const auto a_out = Solution().canPartition({1,2,3,5});
    assert(!a_out);
  
    const auto end_t = std::chrono::high_resolution_clock::now();
    const auto total_t = std::chrono::duration_cast<std::chrono::milliseconds>(end_t - start_t).count();
    std::cerr << total_t << " ms"sv << std::endl;
  }
  std::cerr << "TestSolution OK"sv << std::endl;
}

