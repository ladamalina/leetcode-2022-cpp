class Solution {
public:
  bool canMakeSubsequence(const std::string& str1, const std::string& str2) {
    const auto n = SZ(str1), m = SZ(str2);
    auto i1 = 0, i2 = 0;
    while (i1 < n && i2 < m) {
      if (str1[i1] == str2[i2] || Next(str1[i1]) == str2[i2]) {
        ++i1;
        ++i2;
      } else {
        ++i1;
      }
    }
    return i2 == m;
  }

private:
  static char Next(const char c) {
    return (c == 'z') ? 'a' : static_cast<char>(c + 1);
  }
};

