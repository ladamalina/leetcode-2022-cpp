template<typename T>
std::unordered_set<T> GetFactors(T x) {
  std::unordered_set<T> factors;
  const auto x_sqrt = std::sqrt(x);
  for (T i = 1; i <= x_sqrt; ++i) {
    if (x % i == 0) {
      factors.insert(i);
      factors.insert(x/i);
    }
  }
  return factors;
}

class Solution {
public:
  ll numberOfPairs(const vi& nums1, const vi& nums2, int k) {
    std::unordered_map<int, int> nums2_freq;
    for (const auto x : nums2) ++nums2_freq[x];

    ll res = 0;
    for (const auto x : nums1) {
      if (x%k != 0) continue;
      const auto fs = GetFactors(x/k);
      for (const auto f : fs) {
        if (nums2_freq.contains(f)) res += nums2_freq.at(f);
      }
    }
    return res;
  }
};

