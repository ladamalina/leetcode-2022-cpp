struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

bool DFS(int dst, TreeNode* node, std::string& path) {
  if (node->val == dst) return true;
  if (node->left && DFS(dst, node->left, path)) {
    path.push_back('L');
    return true;
  }
  if (node->right && DFS(dst, node->right, path)) {
    path.push_back('R');
    return true;
  }
  return false;
}

class Solution {
public:
  std::string getDirections(TreeNode* root, int src, int dst) {
    std::string src_path, dst_path;
    DFS(src, root, src_path);
    DFS(dst, root, dst_path);

    while (!src_path.empty() && !dst_path.empty() && src_path.back() == dst_path.back()) {
      src_path.pop_back();
      dst_path.pop_back();
    }

    std::reverse(RNG(dst_path));
    const auto res = (src_path.empty() ? "" : std::string(src_path.size(), 'U')) + dst_path;
    return res;
  }
};

