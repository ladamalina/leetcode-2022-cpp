class NumberContainers {
public:
  NumberContainers() = default;
    
  void change(const int i, const int x) {
    if (idx_x.contains(i)) {
      const auto old_x = idx_x.at(i);
      if (old_x != x) {
        x_ids[old_x].erase(i);
        if (x_ids[old_x].empty())
          x_ids.erase(old_x);
      }
    }
    idx_x[i] = x;
    x_ids[x].insert(i);
  }
    
  int find(const int x) {
    return x_ids.contains(x) ? *x_ids.at(x).begin() : -1;
  }

private:
  std::unordered_map<int, int> idx_x;
  std::unordered_map<int, std::set<int>> x_ids;
};

