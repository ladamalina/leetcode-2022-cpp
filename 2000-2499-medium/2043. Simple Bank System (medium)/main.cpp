class Bank {
public:
  explicit Bank(std::vector<long long>& balance) : n_(balance.size()), b_(balance) {}
    
  bool transfer(int account1, int account2, long long money) {
    if (account1 <= n_ && account2 <= n_ && money <= b_[account1 - 1]) {
      b_[account1 - 1] -= money;
      b_[account2 - 1] += money;
      return true;
    }
    return false;
  }
    
  bool deposit(int account, long long money) {
    if (account <= n_) {
      b_[account - 1] += money;
      return true;
    }
    return false;
  }
    
  bool withdraw(int account, long long money) {
    if (account <= n_ && money <= b_[account - 1]) {
      b_[account - 1] -= money;
      return true;
    }
    return false;
  }

private:
  const int n_;
  std::vector<long long>& b_;
};

