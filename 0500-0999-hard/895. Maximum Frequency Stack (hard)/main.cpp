class FreqStack {
public:
  FreqStack() {
        
  }
    
  void push(const int x) {
    ++ord_;
    
    auto& ords = x_ords_[x];
    ords.push_back(ord_);
    freq_ord_x_.emplace(SZ(ords), ii{ord_, x});
  }

  int pop() {
    const auto it = std::prev(freq_ord_x_.end());
    const auto x = it->S.S;
    x_ords_[x].pop_back();
    freq_ord_x_.erase(it);
    return x;
  }

private:
  int ord_ = 0;
  std::unordered_map<int, vi> x_ords_;
  std::set<std::pair<int, ii>> freq_ord_x_;
};

