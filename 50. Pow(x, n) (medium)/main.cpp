class Solution {
public:
  double myPow(double x, int n) {
    std::unordered_map<ll, double> memo;
    return Pow(x, n, memo);
  }
private:
  double Pow(double x, ll n, std::unordered_map<ll, double>& memo) {
    if (n == 0) return 1;
    if (n == 1) return x;
    if (n < 0) return 1/Pow(x, -n, memo);
    if (memo.contains(n))
      return memo.at(n);

    double res;
    if (n%2 == 0) res = Pow(x, n/2, memo) * Pow(x, n/2, memo);
    else res = x * Pow(x, n-1, memo);
    memo[n] = res;
    return res;
  }
};

