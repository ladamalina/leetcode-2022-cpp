class Solution {
public:
  int longestPalindrome(const std::string& s) {
    const auto max_i = 'z'-'A';
    std::vector<int> freq(max_i+1);
    int len = 0;
    for (const auto c : s) {
      ++freq[c-'A'];
      if (freq[c-'A'] == 2) {
        len += 2;
        freq[c-'A'] = 0;
      }
    }
    for (auto i=0; i<=max_i; ++i) {
      if (freq[i] == 1) {
        len += 1;
        break;
      }
    }
    return len;
  }
};

