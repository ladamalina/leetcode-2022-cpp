class Solution {
public:
  ll splitArray(const vi& nums) {
    const auto n = SZ(nums);
    vb is_prime(n, true);
    is_prime[0] = is_prime[1] = false;
    ll sum_p = 0, sum_np = 0;
    for (auto i = 0; i < n; ++i) {
      if (is_prime[i]) {
        sum_p += nums[i];
        for (auto j = i + i; j < n; j += i) {
          is_prime[j] = false;
        }
      } else {
        sum_np += nums[i];
      }
    }
    return std::abs(sum_p - sum_np);
  }
};

