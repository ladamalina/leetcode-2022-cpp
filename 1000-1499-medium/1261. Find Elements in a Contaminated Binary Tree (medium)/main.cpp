/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */

struct TreeNode {
  int val;
  TreeNode *left;
  TreeNode *right;
  TreeNode() : val(0), left(nullptr), right(nullptr) {}
  explicit TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
  TreeNode(int x, TreeNode* left, TreeNode* right) : val(x), left(left), right(right) {}
};

constexpr int MAX_H = 20;
std::array<int, MAX_H> path;

class FindElements {
public:
  explicit FindElements(TreeNode* root) : root_(root) {}
    
  bool find(int target) {
    if (target == 0)
      return root_;
    // target > 0

    auto n = 0;
    while (target >= 0) {
      path[n++] = target;
      target = (target % 2 == 1) ? (target - 1) / 2 : (target - 2) / 2;
    }

    auto node = root_;
    while (n > 1) {
      if (!node) return false;
      if (path[n-1] * 2 + 1 == path[n-2]) {
        node = node->left;
      } else if (path[n-1] * 2 + 2 == path[n-2]) {
        node = node->right;
      }
      --n;
    }

    return node;
  }

private:
  TreeNode* root_;
};

