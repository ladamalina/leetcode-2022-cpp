bool Check(const vi& bloom_d, int m, int k, int max_d) {
  int ready_m = 0;
  int len = 0;
  for (const auto d : bloom_d) {
    if (d > max_d) len = 0;
    else ++len;
    if (len == k) {
      ++ready_m;
      len = 0;
    }
  }
  if (len == k) ++ready_m;
  return ready_m >= m;
}

class Solution {
public:
  int minDays(const vi& bloom_d, int m, int k) {
    if (CL(m)*k > CL(bloom_d.size())) return -1;

    auto l = 1, r = *std::max_element(RNG(bloom_d));
    while (l<r) {
      const auto mid = (l+r)/2;
      if (Check(bloom_d, m, k, mid)) r = mid;
      else l = mid+1;
    }
    return r;
  }
};

