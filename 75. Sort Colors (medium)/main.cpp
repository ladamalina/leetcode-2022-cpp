class Solution {
public:
  void sortColors(vi& nums) {
    std::array<int, 3> freq{};
    for (const auto x : nums) ++freq[x];
    int i = 0;
    for (auto j=i; j<freq[0]; ++j) { nums[j] = 0; ++i; }
    for (auto j=i; j<freq[0]+freq[1]; ++j) { nums[j] = 1; ++i; }
    for (auto j=i; j<freq[0]+freq[1]+freq[2]; ++j) { nums[j] = 2; ++i; }
  }
};

